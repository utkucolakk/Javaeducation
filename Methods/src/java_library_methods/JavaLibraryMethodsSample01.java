package java_library_methods;

public class JavaLibraryMethodsSample01 {
    public static void main(String[] args) {
        System.out.println(" Math.sqrt() : " + Math.sqrt(16));
        System.out.println(" Math.max() : " + Math.max(5,11));
        System.out.println(" Math.abs() : " + Math.abs(-5));
        System.out.println(" Math.pow() : " + Math.pow(3, 2 ));
    }
}
